LINE_COLORS: dict[str, str] = {**PRIVATE_LINE_COLORS, **JR_LINE_COLORS}

_JR_OPERATORS = ("JR",)
_PRIVATE_OPERATORS = (
    "東武", "西武", "京王", "小田急", "東急", "京成", "京急", "新京成",
    "北総", "芝山", "相模", "相鉄", "東葉", "つくば",
)


def _lookup(name: str, color_map: dict[str, str]) -> str | None:
    if name in color_map:
        return color_map[name]
    best: str | None = None
    best_len = 0
    for key, color in color_map.items():
        if key in name or name in key:
            if len(key) > best_len:
                best_len = len(key)
                best = color
    return best


def _is_jr_line(line_name: str, operator: str | None) -> bool:
    name = line_name.strip()
    op = (operator or "").strip()
    if name.startswith("JR") or "JR" in name:
        return True
    return any(tag in op for tag in _JR_OPERATORS)


def _is_private_line(line_name: str, operator: str | None) -> bool:
    op = (operator or "").strip()
    if any(tag in op for tag in _PRIVATE_OPERATORS):
        return True
    if line_name.startswith("東武"):
        return True
    return False


def resolve_line_color(line_name: str | None, operator: str | None = None) -> str | None:
    if not line_name:
        return None
    name = line_name.strip()
    if not name:
        return None

    if _is_jr_line(name, operator):
        order = (JR_LINE_COLORS, PRIVATE_LINE_COLORS)
    elif _is_private_line(name, operator):
        order = (PRIVATE_LINE_COLORS, JR_LINE_COLORS)
    else:
        order = (JR_LINE_COLORS, PRIVATE_LINE_COLORS)

    for color_map in order:
        found = _lookup(name, color_map)
        if found:
            return found
    return None
